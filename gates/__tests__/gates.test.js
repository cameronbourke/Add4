const testGate = require('../../lib/testGate');

testGate(require('../NAND'), [
	[{ a: '0', b: '0' }, '1'],
	[{ a: '0', b: '1' }, '1'],
	[{ a: '1', b: '0' }, '1'],
	[{ a: '1', b: '1' }, '0'],
]);

testGate(require('../NOT'), [
	[{ a: '0' }, '1'],
	[{ a: '1' }, '0'],
]);

testGate(require('../AND'), [
	[{ a: '0', b: '0' }, '0'],
	[{ a: '0', b: '1' }, '0'],
	[{ a: '1', b: '0' }, '0'],
	[{ a: '1', b: '1' }, '1'],
]);

testGate(require('../OR'), [
	[{ a: '0', b: '0' }, '0'],
	[{ a: '0', b: '1' }, '1'],
	[{ a: '1', b: '0' }, '1'],
	[{ a: '1', b: '1' }, '1'],
]);

testGate(require('../XOR'), [
	[{ a: '0', b: '0' }, '0'],
	[{ a: '0', b: '1' }, '1'],
	[{ a: '1', b: '0' }, '1'],
	[{ a: '1', b: '1' }, '0'],
]);
